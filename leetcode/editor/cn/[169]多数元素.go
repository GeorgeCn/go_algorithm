func majorityElement(nums []int) int {
var mid int
if (len(nums) & 1) == 1 {
mid = len(nums) >> 1 + 1
} else {
mid = len(nums) >> 1
}

m := make(map[int]int)
max := nums[0]
for _, v := range nums{

if counter, ok := m[v]; ok  {
counter += 1
if counter >= mid {
max = v
break
}
m[v] = counter
} else {
m[v] = 1
}
}

return max
}
